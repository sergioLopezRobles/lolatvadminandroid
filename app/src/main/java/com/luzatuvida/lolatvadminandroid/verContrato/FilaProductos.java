package com.luzatuvida.lolatvadminandroid.verContrato;

public class FilaProductos {

    private String idProducto;
    private String idProductoContrato;
    private String producto;
    private String precio;
    private String piezas;
    private String total;

    public String getIdProducto() {
        return idProducto;
    }

    public void setIdProducto(String idProducto) {
        this.idProducto = idProducto;
    }

    public String getIdProductoContrato() {
        return idProductoContrato;
    }

    public void setIdProductoContrato(String idProductoContrato) {
        this.idProductoContrato = idProductoContrato;
    }

    public String getProducto() {
        return producto;
    }

    public void setProducto(String producto) {
        this.producto = producto;
    }

    public String getPrecio() {
        return precio;
    }

    public void setPrecio(String precio) {
        this.precio = precio;
    }

    public String getPiezas() {
        return piezas;
    }

    public void setPiezas(String piezas) {
        this.piezas = piezas;
    }

    public String getTotal() {
        return total;
    }

    public void setTotal(String total) {
        this.total = total;
    }
}
