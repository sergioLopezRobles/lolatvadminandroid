package com.luzatuvida.lolatvadminandroid.verContrato;

public class FilaAbonos {

    private String idAbono;
    private String abono;
    private String tipoAbono;

    private String fechaAbono;
    private String folioAbono;
    private String metodoPago;

    public String getIdAbono() {
        return idAbono;
    }

    public void setIdAbono(String idAbono) {
        this.idAbono = idAbono;
    }

    public String getAbono() {
        return abono;
    }

    public void setAbono(String abono) {
        this.abono = abono;
    }

    public String getTipoAbono() {
        return tipoAbono;
    }

    public void setTipoAbono(String tipoAbono) {
        this.tipoAbono = tipoAbono;
    }

    public String getFechaAbono() {
        return fechaAbono;
    }

    public void setFechaAbono(String fechaAbono) {
        this.fechaAbono = fechaAbono;
    }

    public String getFolioAbono() {
        return folioAbono;
    }

    public void setFolioAbono(String folioAbono) {
        this.folioAbono = folioAbono;
    }

    public String getMetodoPago() { return metodoPago;}

    public void setMetodoPago(String metodoPago){ this.metodoPago = metodoPago;}
}
